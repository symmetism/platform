"""`sym scaffold <repo>/<app-name>` — generate a Symmetism-tracked app skeleton.

Battle-plan F11.2 says: an operator should get a runnable, manifest-
exposing FastAPI app + Dockerfile + Caddy block in one command. The
GHA build-and-publish workflow already detects new `apps/<name>/`
dirs that contain a Dockerfile, so the only manual step after
scaffolding is editing the app's logic and pushing.

What this writes (under <repo>/apps/<app-name>/):

  pyproject.toml          minimal hatchling FastAPI project
  Dockerfile              two-stage python:3.12-slim, healthcheck on /healthz
  README.md               1-paragraph stub
  src/<pkg>/__init__.py   __version__ = "0.1.0"
  src/<pkg>/main.py       FastAPI app; /healthz; attach_manifest_endpoint
  src/<pkg>/manifest_endpoint.py
                          self-contained /__manifest helper (FastAPI flavor)
  caddy-snippet.txt       reverse_proxy block for the operator to merge
                          into server/Caddyfile by hand

Idempotent: if the target dir already exists, scaffold refuses to clobber
unless `force=True`. We never write to other repos' state — only the
repo path resolved from <repo>.
"""

from __future__ import annotations

import re
from dataclasses import dataclass
from pathlib import Path

from symverify import config


class ScaffoldError(RuntimeError):
    """Raised when scaffolding cannot proceed (e.g. dir exists)."""


_NAME_RE = re.compile(r"^[a-z][a-z0-9-]{1,40}$")


def _validate_app_name(name: str) -> None:
    if not _NAME_RE.match(name):
        raise ScaffoldError(
            f"app name {name!r} must match {_NAME_RE.pattern} "
            "(lowercase, dashes ok, 2-41 chars, starts with letter)"
        )


def _pkg_name(app_name: str) -> str:
    """Convert dash-name to snake-name for the Python package."""
    return app_name.replace("-", "_")


