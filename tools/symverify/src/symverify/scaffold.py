# Result type + main entry
# ---------------------------------------------------------------------------


@dataclass(slots=True)
class ScaffoldResult:
    """Files written + summary."""

    app_name: str
    repo_name: str
    app_dir: Path
    files_written: list[Path]
    caddy_snippet_path: Path


def scaffold_app(
    target: str,
    *,
    force: bool = False,
) -> ScaffoldResult:
    """Generate a new app skeleton at <repo>/apps/<app-name>/."""
    if "/" not in target:
        raise ScaffoldError("expected <repo>/<app-name> (e.g. platform/foo)")
    repo_name, app_name = target.split("/", 1)
    _validate_app_name(app_name)
    pkg_name = _pkg_name(app_name)

    repos = config.load_repos()
    if repo_name not in repos:
        raise ScaffoldError(
            f"repo '{repo_name}' not configured; have: {sorted(repos)}"
        )
    repo_path = repos[repo_name].path
    app_dir = repo_path / "apps" / app_name

    if app_dir.exists() and not force:
        raise ScaffoldError(f"refusing to clobber existing dir {app_dir} (pass force=True)")

    app_dir.mkdir(parents=True, exist_ok=True)
    src_dir = app_dir / "src" / pkg_name
    src_dir.mkdir(parents=True, exist_ok=True)

    written: list[Path] = []

    def _write(path: Path, content: str) -> None:
        path.write_text(content, encoding="utf-8")
        written.append(path)

    _write(app_dir / "pyproject.toml", _pyproject(app_name, pkg_name))
    _write(app_dir / "Dockerfile", _dockerfile(app_name, pkg_name, repo_name))
    _write(app_dir / "README.md", _readme(app_name))
    _write(src_dir / "__init__.py", _init_py(app_name))
    _write(src_dir / "main.py", _main_py(app_name, pkg_name))
    _write(src_dir / "manifest_endpoint.py", _manifest_endpoint_py(pkg_name))

    caddy_path = app_dir / "caddy-snippet.txt"
    _write(caddy_path, _caddy_snippet(app_name))

    return ScaffoldResult(
        app_name=app_name,
        repo_name=repo_name,
        app_dir=app_dir,
        files_written=written,
        caddy_snippet_path=caddy_path,
    )
